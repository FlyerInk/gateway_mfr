-module(gateway_mfr_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    case gateway_mfr_sup:start_link() of
        {ok, Pid} ->
            gateway_mfr_cli_registry:register_cli(),
            {ok, Pid};
         Other -> Other
    end.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

